/**
 * https://discord.com/developers/docs/resources/channel#embed-object-embed-types
 */
export enum EmbedTypes {
  /** Generic embed rendered from embed attributes. */
  Rich = "rich",
  /** Image embed. */
  Image = "image",
  /** Video embed. */
  Video = "video",
  /** Animated gif image embed rendered as a video embed. */
  GifV = "gifv",
  /** Article embed. */
  Article = "article",
  /** Link embed. */
  Link = "link",
}
